function figletRender(text: string): TerminalOutputLine[] {
  const chars = text
    .toUpperCase()
    .split("")
    .map((c) => FIGLET_FONT[c] ?? FIGLET_FONT["?"]);
  return Array.from({ length: 5 }, (_, row) => ({
    content: chars.map((c) => c[row]).join(" "),
  }));
}

const figletCommand: Command = {
  name: "figlet",
  aliases: [],
  description: "render text as large ASCII art letters",
  usage: "figlet <text>",
  execute(args, _flags, stdin) {
    markEggFound("figlet");
    const text = args.length > 0 ? args.join(" ") : (stdin ?? "");
    if (!text.trim()) return errOut("figlet: usage: figlet <text>");
    return { lines: figletRender(text), exitCode: 0 };
  },
};

// --- Register ---

registry.register(figletCommand);
